"""
Конфигурация стилей статей — Style Fingerprinting.

Каждый стиль описывает:
- Структурный паттерн (какие блоки, в каком порядке)
- Тональность и стиль изложения
- Целевой размер (символы) с возможностью ручного переопределения
- Визуальные элементы (картинки, таблицы)
- Примеры (для RAG-контекста Heart)

ВАЖНО: Агенты берут ТОЛЬКО стилистику и структуру.
       Контент и факты генерируются заново.
"""
from dataclasses import dataclass, field
from typing import List, Optional, Dict


@dataclass
class ArticleStyle:
    """Описание стиля статьи."""
    id: str                          # Уникальный ID стиля
    name: str                        # Название для UI
    description: str                 # Описание для промпта
    folder_name: str                 # Папка в Article Styles/

    # Размеры (символы)
    target_chars: int                # Целевой объём по умолчанию
    min_chars: int                   # Минимум
    max_chars: int                   # Максимум

    # Структурные паттерны
    structure: List[str]             # Порядок блоков
    has_images: bool = True          # Картинки между разделами
    has_tables: bool = False         # Таблицы в тексте
    has_toc: bool = True             # Содержание в начале

    # Тональность
    tone: str = "экспертный"         # Тон текста
    pov: str = "безличный"           # Точка зрения (безличный / от 1 лица / обращение)

    # Примеры файлов (для RAG)
    examples: List[str] = field(default_factory=list)

    # Динамические промпты (CMS)
    engineer_instruction: Optional[str] = None
    heart_instruction: Optional[str] = None
    sheriff_instruction: Optional[str] = None
    style_reference_prompt: Optional[str] = None
    text_overlay_word: Optional[str] = None


