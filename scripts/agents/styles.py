def get_style(style_id: str) -> ArticleStyle:
    """Получить стиль по ID."""
    style = STYLES.get(style_id)
    if not style:
        available = ", ".join(STYLES.keys())
        raise ValueError(f"Стиль '{style_id}' не найден. Доступные: {available}")
    return style


def get_style_prompt(style_id: str, custom_chars: int = None) -> str:
    """
    Сформировать блок промпта со стилистическими инструкциями.

    Args:
        style_id: ID стиля
        custom_chars: пользовательский объём символов (из дашборда)

    Returns:
        Текстовый блок для вставки в промпт Heart
    """
    style = get_style(style_id)
    chars = custom_chars or style.target_chars
    min_c = int(chars * 0.9) if custom_chars else style.min_chars
    max_c = int(chars * 1.1) if custom_chars else style.max_chars

    heart_inst_block = ""
    if style.heart_instruction:
        heart_inst_block = f"\n\n### Индивидуальное руководство по написанию\n{style.heart_instruction}"

    prompt = f"""
## СТИЛЬ СТАТЬИ: {style.name}

### Описание
{style.description}

### Объём
- Целевой: {chars} символов
- Допустимый диапазон: {min_c}–{max_c} символов
- СТРОГО: не выходить за границы диапазона

### Структура
{chr(10).join(f'{i+1}. {s}' for i, s in enumerate(style.structure))}

### Тональность
- Тон: {style.tone}
- Точка зрения: {style.pov}

### Визуальные элементы
- Картинки между разделами: {'да — вставляй маркеры [картинка: описание]' if style.has_images else 'нет'}
- Таблицы: {'да — используй Markdown-таблицы' if style.has_tables else 'нет, если не требуется по контексту'}
- Содержание в начале: {'да' if style.has_toc else 'нет'}{heart_inst_block}

### КРИТИЧЕСКОЕ ПРАВИЛО
Ты НЕ копируешь текст из примеров. Ты берёшь ТОЛЬКО:
1. Структурный паттерн (порядок блоков)
2. Тональность и стиль изложения
3. Глубину проработки каждого блока
4. Формат подачи (нумерация, маркеры, таблицы)
ВСЕ ФАКТЫ, ЗАКОНЫ, ЦИФРЫ И КЕЙСЫ — генерируешь заново на основе контекста из базы знаний.
"""
    return prompt.strip()


def list_styles() -> List[Dict]:
    """Список стилей для дашборда."""
    return [
        {
            "id": s.id,
            "name": s.name,
            "target_chars": s.target_chars,
            "min_chars": s.min_chars,
            "max_chars": s.max_chars,
            "has_tables": s.has_tables,
            "has_images": s.has_images,
        }
        for s in STYLES.values()
    ]


# Попытка подгрузить внешние настройки для админки
try:
    import json
    from pathlib import Path
    config_path = Path(__file__).resolve().parent.parent.parent / "config" / "styles_config.json"
    if config_path.exists():
        with open(config_path, "r", encoding="utf-8") as f:
            ext_config = json.load(f)
            for k, v in ext_config.get("article_types", {}).items():
                if k not in STYLES:
                    STYLES[k] = ArticleStyle(
                        id=k,
                        name=v.get("name", k),
                        description=v.get("description", ""),
                        folder_name=v.get("folder_name", k),
                        target_chars=v.get("target_chars", 15000),
                        min_chars=v.get("min_chars", 10000),
                        max_chars=v.get("max_chars", 25000),
                        structure=v.get("structure", ["intro", "h2_sections", "conclusion"]),
                    )
                
                # Числовые лимиты
                STYLES[k].target_chars = v.get("target_chars", STYLES[k].target_chars)
                STYLES[k].min_chars = v.get("min_chars", STYLES[k].min_chars)
                STYLES[k].max_chars = v.get("max_chars", STYLES[k].max_chars)
                
                # Основные параметры
                if "name" in v:
                    STYLES[k].name = v["name"]
                if "description" in v:
                    STYLES[k].description = v["description"]
                if "has_images" in v:
                    STYLES[k].has_images = bool(v["has_images"])
                if "has_tables" in v:
                    STYLES[k].has_tables = bool(v["has_tables"])
                if "has_toc" in v:
                    STYLES[k].has_toc = bool(v["has_toc"])
                if "tone" in v:
                    STYLES[k].tone = v["tone"]
                if "pov" in v:
                    STYLES[k].pov = v["pov"]
                    
                # Динамические промпты для агентов
                prompts = v.get("prompts", {})
                if "engineer_instruction" in prompts:
                    STYLES[k].engineer_instruction = prompts["engineer_instruction"]
                if "heart_instruction" in prompts:
                    STYLES[k].heart_instruction = prompts["heart_instruction"]
                if "sheriff_instruction" in prompts:
                    STYLES[k].sheriff_instruction = prompts["sheriff_instruction"]
                if "style_reference_prompt" in prompts:
                    STYLES[k].style_reference_prompt = prompts["style_reference_prompt"]
                if "text_overlay_word" in prompts:
                    STYLES[k].text_overlay_word = prompts["text_overlay_word"]
except Exception as e:
    pass
